const AKHAND_BHARAT_CENTER = { lat: 23.5, lon: 78.5 };

function toPointsArray(
  coords: [number, number][],
  radius: number,
  alt: number
): [number, number, number][] {
  return coords.map(([lat, lon]) => {
    const v = latLonToVec3(lat, lon, radius + alt);
    return [v.x, v.y, v.z];
  });
}

interface IndiaBorderProps {
  radius: number;
}

export const IndiaBorder = ({ radius }: IndiaBorderProps) => {
  const groupRef = useRef<THREE.Group>(null);
  const textRef = useRef<THREE.Mesh>(null);
  const ALT = 0.015;

  const mainlandPts = useMemo(() => toPointsArray(MAINLAND, radius, ALT), [radius]);
  const sriLankaPts = useMemo(() => toPointsArray(SRI_LANKA, radius, ALT), [radius]);
  const islandsPts = useMemo(() => ISLANDS.map(island => toPointsArray(island, radius, ALT)), [radius]);

  // Text position & orientation
  const textPos = useMemo(
    () => latLonToVec3(AKHAND_BHARAT_CENTER.lat, AKHAND_BHARAT_CENTER.lon, radius + ALT + 0.01),
    [radius]
  );
  const textQuaternion = useMemo(() => {
    const normal = textPos.clone().normalize();
    const up = new THREE.Vector3(0, 1, 0);
    const tangentY = up.clone().projectOnPlane(normal).normalize();
    const tangentX = new THREE.Vector3().crossVectors(tangentY, normal).normalize();
    const matrix = new THREE.Matrix4().makeBasis(tangentX, tangentY, normal);
    return new THREE.Quaternion().setFromRotationMatrix(matrix);
  }, [textPos]);

  // Fade based on camera facing
  useFrame(({ camera }) => {
    if (!groupRef.current) return;

    const centerWorld = textPos
      .clone()
      .applyMatrix4(groupRef.current.parent?.matrixWorld ?? new THREE.Matrix4());
    const dirToCamera = camera.position.clone().sub(centerWorld).normalize();
    const surfaceNormal = centerWorld.clone().normalize();
    const dot = surfaceNormal.dot(dirToCamera);

    const opacity = THREE.MathUtils.clamp(
      THREE.MathUtils.mapLinear(dot, -0.2, 0.5, 0, 0.85),
      0,
      0.85
    );

    // Fade all line children and text
    groupRef.current.traverse((child) => {
      const obj = child as THREE.Mesh;
      if (obj?.material && "opacity" in obj.material) {
        (obj.material as { opacity: number }).opacity = opacity;
      }
    });

    if (textRef.current) {
      const textMat = textRef.current.material as THREE.MeshBasicMaterial;
      if (textMat) textMat.opacity = opacity * 1.0;
    }
  });

  const borderColor = "#f97316";

  return (
    <group ref={groupRef}>
      {/* Mainland border */}
      <Line
        points={mainlandPts}
        color={borderColor}
        lineWidth={1.0}
        transparent
        opacity={0.5}
        depthTest={false}
        renderOrder={995}
      />

      {/* Sri Lanka */}
      <Line
        points={sriLankaPts}
        color={borderColor}
        lineWidth={1.2}
        transparent
        opacity={0.2}
        depthTest={false}
        renderOrder={995}
      />

      {/* Islands */}
      {islandsPts.map((pts, i) => (
        <Line
          key={i}
          points={pts}
          color={borderColor}
          lineWidth={0.5}
          transparent
          opacity={0.2}
          depthTest={false}
          renderOrder={995}
        />
      ))}

      {/* भारत label */}
      <Text
        ref={textRef}
        position={textPos}
        quaternion={textQuaternion}
        fontSize={0.32}
        color="#ff9933"
        anchorX="center"
        anchorY="middle"
        renderOrder={996}
        font="https://fonts.gstatic.com/s/yatraone/v16/C8ch4copsHzj8p7NaF0xww.ttf"
        outlineWidth={0.05}
        outlineColor="#111827"
        outlineOpacity={0.25}
        material-transparent
        material-opacity={0.75}
        material-depthTest={false}
      >
        भारत
      </Text>
    </group>
  );
};
